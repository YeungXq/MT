package com.mt.comment.service.impl;

import com.mt.comment.dao.CommentDao;
import com.mt.pojo.Comment;
import com.mt.comment.pojo.CommentDTO;
import com.mt.comment.service.CommentService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;

@Service
public class CommentServiceImpl implements CommentService {
    @Autowired
    CommentDao commentDao;

    /**
     * 客户端-获取电影具体信息
     * 评论列表
     */
    @Override
    public List<CommentDTO> listComment(Integer filmId) {
        return commentDao.listComment(filmId);
    }



    /**
     * 新增评论
     */
    @Override
    public boolean insertComment(Comment comment) {
        return commentDao.insertComment(comment);
    }



    @Override
    public boolean updateComment(Integer commentId, String content) {
        return commentDao.updateComment(commentId, content);
    }

    public boolean isComment(Integer filmId, String customerId) {
        Comment comment= commentDao.getComment(filmId,customerId);
        if (comment != null) return false;
         return true;
    }

}
